"""## 11. Exact + alias + fuzzy dish matching

Matching logic:
1. **Exact match** against restaurant menu dishes
2. **Alias match**
3. **Fuzzy match** against the sentence tokens / n-grams

Only restaurant-specific dishes are considered.

"""

def token_ngrams(tokens, min_n=1, max_n=4):
    grams = []
    n_tokens = len(tokens)
    for n in range(min_n, min(max_n, n_tokens) + 1):
        for i in range(n_tokens - n + 1):
            grams.append(" ".join(tokens[i:i+n]))
    return grams

def exact_or_alias_match(sentence_norm: str, dishes_for_restaurant):
    matches = []
    for dish in dishes_for_restaurant:
        dish_norm = dish["dish_name_norm"]
        canonical = dish["dish_canonical"]

        if dish_norm and dish_norm in sentence_norm:
            matches.append({
                "matched_phrase": dish_norm,
                "dish_name": dish["dish_name"],
                "dish_canonical": canonical,
                "match_type": "exact",
                "match_score": 100
            })
            continue

        for can_dish, aliases in ALIASES_NORM.items():
            if canonicalize_dish_name(dish["dish_name"]) == can_dish:
                for alias in aliases:
                    if alias and alias in sentence_norm:
                        matches.append({
                            "matched_phrase": alias,
                            "dish_name": dish["dish_name"],
                            "dish_canonical": canonical,
                            "match_type": "alias",
                            "match_score": 100
                        })
                        break
    return matches

def fuzzy_match(sentence_norm: str, dishes_for_restaurant, threshold=90):
    matches = []
    tokens = sentence_norm.split()
    grams = token_ngrams(tokens, min_n=1, max_n=4)

    for dish in dishes_for_restaurant:
        dish_target = dish["dish_name_norm"]
        if not dish_target:
            continue

        best_score = -1
        best_phrase = None

        for gram in grams:
            score = fuzz.ratio(gram, dish_target)
            if score > best_score:
                best_score = score
                best_phrase = gram

        if best_score >= threshold:
            matches.append({
                "matched_phrase": best_phrase,
                "dish_name": dish["dish_name"],
                "dish_canonical": dish["dish_canonical"],
                "match_type": "fuzzy",
                "match_score": best_score
            })

    return matches

def deduplicate_matches(match_list):
    seen = set()
    deduped = []
    for m in match_list:
        key = (m["dish_canonical"], m["match_type"], m["matched_phrase"])
        if key not in seen:
            deduped.append(m)
            seen.add(key)
    return deduped

"""## 12. Run sentence-to-dish linking

By default:
- exact + alias matching are always used
- fuzzy matching is used only if exact/alias finds nothing

You can relax or tighten this behavior later.

"""

linked_rows = []

for _, row in tqdm(sentences_df.iterrows(), total=len(sentences_df)):
    restaurant_norm = row["restaurant_name_norm"]
    dishes_for_restaurant = restaurant_to_dishes.get(restaurant_norm, [])

    if not dishes_for_restaurant:
        continue

    sentence_norm = row["sentence_norm"]

    matches = exact_or_alias_match(sentence_norm, dishes_for_restaurant)

    if not matches:
        matches = fuzzy_match(sentence_norm, dishes_for_restaurant, threshold=90)

    matches = deduplicate_matches(matches)

    for m in matches:
        linked_rows.append({
            "review_row_id": row["review_row_id"],
            "restaurant_name": row["restaurant_name"],
            "review_url": row["review_url"],
            "stars": row["stars"],
            "sentence_id_in_review": row["sentence_id_in_review"],
            "sentence": row["sentence"],
            "sentence_norm": row["sentence_norm"],
            "has_sentiment_cue": row["has_sentiment_cue"],
            "matched_phrase": m["matched_phrase"],
            "dish_name_from_menu": m["dish_name"],
            "dish_canonical": m["dish_canonical"],
            "match_type": m["match_type"],
            "match_score": m["match_score"],
        })

