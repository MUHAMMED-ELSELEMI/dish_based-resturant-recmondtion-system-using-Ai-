"""## 8. Split reviews into sentences

This is a lightweight sentence splitter based on punctuation.
It is simple but works reasonably well for noisy review text.

"""

SENTENCE_SPLIT_PATTERN = re.compile(r"(?<=[.!?؟])\s+|\n+")

def split_into_sentences(text: str):
    if pd.isna(text):
        return []
    text = str(text).strip()
    if not text:
        return []
    parts = re.split(SENTENCE_SPLIT_PATTERN, text)
    parts = [p.strip(" -•\t\r\n") for p in parts if p and p.strip(" -•\t\r\n")]
    return parts

