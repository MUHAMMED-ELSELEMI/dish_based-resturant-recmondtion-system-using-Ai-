"""## 5. Normalize restaurant names and dish names

We create normalized versions for matching:
- lowercase
- remove extra spaces
- remove repeated punctuation
- keep a canonical dish form for later export

"""

def normalize_text(text: str) -> str:
    if pd.isna(text):
        return ""
    text = str(text).strip().lower()
    text = re.sub(r"http\S+|www\.\S+", " ", text)
    text = re.sub(r"[_|]+", " ", text)
    text = re.sub(r"\s*/\s*", " ", text)
    text = re.sub(r"[^\w\s\-']", " ", text)
    text = re.sub(r"\s+", " ", text).strip()
    return text

DISH_MODIFIER_STOPWORDS = {
    "large", "small", "medium", "big", "regular",
    "spicy", "hot", "extra", "special", "classic",
    "plate", "sandwich", "wrap", "combo", "meal",
    "double", "single", "with", "and", "set"
}

def canonicalize_dish_name(text: str) -> str:
    text = normalize_text(text)
    tokens = [t for t in text.split() if t not in DISH_MODIFIER_STOPWORDS]
    return " ".join(tokens).strip()

menus_df["restaurant_name_norm"] = menus_df["restaurant_name"].map(normalize_text)
